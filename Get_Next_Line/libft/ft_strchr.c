#include "includes/libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while ((s[i] != '\0') && (s[i] != (char)c))
		s++;
	if (s[i] == (char)c)
		return ((char*)s);
	return (0);
}
